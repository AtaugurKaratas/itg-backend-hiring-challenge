package com.itg.supplychainmanagement.model;

public class ProductImage {
    private int id;
    private String path;

    public ProductImage(int id, String path) {
        this.id = id;
        this.path = path;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
